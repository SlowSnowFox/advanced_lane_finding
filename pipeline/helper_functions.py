def combine_images(img1, img2, img3, img4):
    """
    Adds the X different versions of the output together and displays them
    in 1 single window
    """
    cc_images = []
    for image in [img1, img2, img3, img4]:
        if len(image.shape) != 3 or image.shape[2] != 3:
            image = cv2.cvtColor(image, cv2.COLOR_GRAY2BGR)
        cc_images.append(image)
    upper_comb_img = np.hstack((cc_images[0], cc_images[1]))
    lower_comb_img = np.concatenate((cc_images[2], cc_images[3]), axis = 1)
    comb_img = np.concatenate((upper_comb_img, lower_comb_img), axis=0)
    return comb_img
